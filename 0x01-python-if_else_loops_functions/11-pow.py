#!/usr/bin/python3
def pow(a, b):
    """
    Computes a to the power of b and returns the value.

    :param a: The base.
    :type a: int
    :param b: The exponent.
    :type b: int
    :return: The result of a raised to the power of b.
    :rtype: int
    """
    result = a ** b
    return result
